#!/usr/bin/env python3
"""Prose-coverage guard for the DNA docs (s-docs-coverage-guard). Pure stdlib.

The autogenerated Reference (docs/reference/**) can never drift — the drift
guard in docs.yml regenerates it from source and diffs. But the PROSE can:
search/memory shipped with zero concept/guide pages and nobody noticed until
a human looked. This guard closes that hole the same way: the sources of
truth are machine-readable, so prose COVERAGE is checkable.

What it enumerates (from source, never from the docs):
  * top-level ``dna`` CLI groups — introspected from the live Click tree
    (``dna_cli.main``), the same mechanism as ``scripts/gen_cli_docs.py``;
  * registered Kinds — introspected from ``Kernel.auto()``, the same
    mechanism as ``scripts/gen_kinds_docs.py``.

What counts as PROSE: ``docs/concepts/**``, ``docs/guides/**``,
``docs/getting-started/**`` — plus ``README.md``, which is deliberately
counted as valid prose (it is hand-written narrative and often the first
page a user reads). ``docs/reference/**`` NEVER counts: it is generated
from the same source being checked, so it would satisfy the guard
tautologically.

What counts as a MENTION (deliberately narrow — precision over recall):
  * a CLI group is covered only by an invocation-shaped mention,
    ``dna <group>`` (case-insensitive). Bare words like "search" or
    "memory" occur in ordinary prose constantly; counting them would have
    hidden exactly the search/memory gap that motivated this guard.
  * a Kind is covered by its exact CamelCase name as a whole word,
    case-SENSITIVELY, plural tolerated ("Stories" does not match — but
    "Epics"/"Features" do), or by its globally-unique alias
    (case-insensitive). Case-sensitivity matters for the same reason:
    "plan", "comment", "setting", "hook" are common English words.

Allowlist is EXPLICIT and versioned here, one justification string per
item, brand_guard style. Shrink-only by convention: entries are removed as
prose lands, never grown to paper over a new gap (a NEW public surface must
ship with prose or a justified entry reviewed on the PR). If an allowlisted
item becomes covered by prose, the guard prints a stale-entry NOTE asking
for its removal (a nudge, not a failure — so a sister docs PR can merge
first and the removal follow in its wake).

Usage:
    python3 scripts/docs_coverage_guard.py               # run the guard (exit 1 on gap)
    python3 scripts/docs_coverage_guard.py --self-test   # planted-gap self-tests
    python3 scripts/docs_coverage_guard.py --all         # report-only: ignore the allowlist

CI runs ``--self-test`` first, then the real guard (docs-build job in
.github/workflows/docs.yml, after the reference drift guard).
"""
